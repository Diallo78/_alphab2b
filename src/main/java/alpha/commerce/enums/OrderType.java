package alpha.commerce.enums;

public enum OrderType {
    STANDARD, EXPRESS, INTERNATIONAL

    /*
        STANDARD: Avec la livraison standard, qui est la livraison par defaut. Elle est souvent moins couteuse et
                  prend plus de temps par rapport aux autres option
        EXPRESS : Elle est souvent utilisée lorsque le client a besoin de recevoir les produit plus rapidement est
                  est pret a payer des frais de livraison. Aventage plus rapide
        INTERNATIONAL : Délais de livraison plus longs et des frais de livraison plus élevés en raison des distances
                  plus grande(Pays - Pays) et des formalités douanières
     */
}
